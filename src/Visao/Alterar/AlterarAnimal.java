    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        String codigooo = codigo.getText();
        String nome = jTF_nome.getText();
        String tipo = jTF_tipo.getText();
        String raca = jTF_raca.getText();
        String tamanho = jTF_tamanho.getText();
        String peso = jTF_peso.getText();
        String idade = jTF_idade.getText();
        
        if (nome.equalsIgnoreCase("") || tipo.equalsIgnoreCase("") || raca.equalsIgnoreCase("") || tamanho.equalsIgnoreCase("") || peso.equalsIgnoreCase("") || idade.equalsIgnoreCase("")) {
            
            JOptionPane.showMessageDialog(null,"Nenhum campo pode ficar vazio!!","Erro", JOptionPane.WARNING_MESSAGE);
            

        }
        else{
            Connection con = Conexao.AbrirConexao(); 
            AnimalDAO sql = new AnimalDAO(con);
            Animal a = new Animal();
            
            int cod = Integer.parseInt(codigooo);
            double tamanhoo = Double.parseDouble(tamanho);
            double pesoo = Double.parseDouble(peso);
            int idadee = Integer.parseInt(idade);
            
            a.setCod(cod);
            a.setNome(nome);
            a.setTipo(tipo);
            a.setRaca(raca);
            a.setTamanho(tamanhoo);
            a.setPeso(pesoo);
            a.setIdade(idadee);
            
            sql.Alterar_Animal(a);
            
            Conexao.FecharConexao(con);
            
            jTF_nome.setText("");
            jTF_tipo.setText("");
            jTF_raca.setText("");
            jTF_tamanho.setText("");
            jTF_peso.setText("");
            jTF_idade.setText("");
            jTF_idade.setText("");
            codigo.setText("");
            jTF_cod.setText("");
            
            JOptionPane.showMessageDialog(null,"Animal alterado com sucesso", "sucesso", JOptionPane.INFORMATION_MESSAGE);
        }
    }//GEN-LAST:event_jButton2ActionPerformed

