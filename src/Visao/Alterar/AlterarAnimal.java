    private void InserirDados(int cod){
        
        Connection con = Conexao.AbrirConexao();
        AnimalDAO sql = new AnimalDAO(con);
        List<Animal> lista = new ArrayList<>();
        lista = sql.CapturarAnimal(cod);
        
        for(Animal a : lista){
            codigo.setText(""+ a.getCod());
            jTF_nome.setText(a.getNome());
            jTF_tipo.setText(a.getTipo());
            jTF_raca.setText(a.getRaca());
            jTF_tamanho.setText(""+ a.getTamanho());
            jTF_peso.setText(""+ a.getPeso());
            jTF_idade.setText(""+ a.getIdade());
        }
        Conexao.FecharConexao(con);
        
    }
    
